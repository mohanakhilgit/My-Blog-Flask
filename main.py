@app.route('/register', methods=['GET', 'POST'])
def register():
    new_user_form = RegisterForm()
    if new_user_form.validate_on_submit():
        user = User.query.filter_by(email=new_user_form.email.data).first()
        if user:
            flash("Email already exist..!, Try Logging in.", "error")
            return redirect(url_for('login'))
        hashed_and_salted_pass = generate_password_hash(
            new_user_form.password.data,
            method='pbkdf2:sha256',
            salt_length=8
        )
        new_user = User()
        new_user.email = new_user_form.email.data
        new_user.name = new_user_form.name.data
        new_user.password = hashed_and_salted_pass
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=new_user_form)


@app.route('/login', methods=['GET', 'POST'])
def login():
    new_login_form = LoginForm()
    if new_login_form.validate_on_submit():
        email = new_login_form.email.data
        password = new_login_form.password.data
        user = User.query.filter_by(email=email).first()
        if user:
            if check_password_hash(user.password, password):
                login_user(user)
                return redirect(url_for('get_all_posts'))
            else:
                flash("The password you entered is incorrect, Please try again.", 'error')
                return redirect(url_for('login'))
        else:
            flash("User doesn't exist..!, Please try again.")
    return render_template("login.html", form=new_login_form)


@app.route('/logout')
def logout():
    logout_user()
    return redirect(url_for('get_all_posts'))


@app.route("/post/<int:post_id>", methods=['GET', 'POST'])
def show_post(post_id):
    new_comment_form = CommentForm()
    requested_post = BlogPost.query.get(post_id)
    if new_comment_form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("Please login to comment on a post.", "error")
            return redirect(url_for('login'))
        new_comment = Comment(
            author_id=current_user.id,
            post_id=post_id,
            text=new_comment_form.body.data
        )
        db.session.add(new_comment)
        db.session.commit()
        return redirect(url_for('show_post', post_id=post_id))
    return render_template("post.html", post=requested_post, form=new_comment_form)


